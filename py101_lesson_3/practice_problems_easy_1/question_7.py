flintstones = ["Fred", "Barney", "Wilma", "Betty", "Bambam", "Pebbles"]
# append() adds to the end of list
flintstones.append('Dino')
print(flintstones)
# insert() allows you to add string at desired index
flintstones.insert(3, 'Dino2')
print(flintstones)
# string concatenation add the string to the end of the list
print(flintstones + ['Dino3'])
