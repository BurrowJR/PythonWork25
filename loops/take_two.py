# Write a for loop that iterates over the integers form 1 to 100 and prints the result of multiplying each integer by 2.

for num in range(1, 101):
    result = (num * 2)
    print(result)

# The 'for' loop on line 3 is used to iterate over a sequence of numbers in range(1, 101). The 'range generates a sequence of numbers starting from 1 to 101 (exclusive). It wil iterate from 1 to 100.
# On line 4 the 'result' variable is initialized inside the loop to equal each iterated number multiplied by 2.
# On line 5, inside the loop, the 'print(result)' statement outputs the value of 'result' to the console.  This happens for each iteration of the loop.